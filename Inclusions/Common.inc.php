function remplace_car_accentues_et_maj($chaine) {
    $chaine = trim($chaine);
    $foo = array(
        'à' => 'a',
        'é' => 'e',
        'è' => 'e',
        'ï' => 'i',
        'î' => 'i',
        'ô' => 'o',
        'ö' => 'o',
        'ù' => 'u',
        'û' => 'u',
        'ü' => 'u',
        'A' => 'a',
        'B' => 'b',
        'C' => 'c',
        'D' => 'd',
        'E' => 'e',
        'F' => 'f',
        'G' => 'g',
        'H' => 'h',
        'I' => 'i',
        'J' => 'j',
        'K' => 'k',
        'L' => 'l',
        'M' => 'm',
        'N' => 'n',
        'O' => 'o',
        'P' => 'p',
        'Q' => 'q',
        'R' => 'r',
        'S' => 's',
        'T' => 't',
        'U' => 'u',
        'V' => 'v',
        'W' => 'w',
        'X' => 'x',
        'Y' => 'y',
        'Z' => 'z'
    );

    foreach($foo as $ancien => $nouveau) {
        $chaine = str_replace($ancien, $nouveau,$chaine);
    }
    return $chaine;
}


